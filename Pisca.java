package ccme;

public class Pisca {
	
	private boolean setaDireita = false;
	private boolean setaEsquerda = false;
	
	
	
	void DarSetaDireita() {
		for(int a = 0; a < 20; a++)
			if(setaDireita = false){
				setaDireita = true;
			}
			if(setaDireita = true) {
				setaDireita = false;
			}
	}
	
	void DarSetaEsquerda() {
		for(int a = 0; a < 20; a++)
			if(setaEsquerda = false){
				setaEsquerda = true;
			}
			if(setaEsquerda = true) {
				setaEsquerda = false;
			}
	}
	
	void PiscaAlarme() {
		for(int a = 0; a < 5; a++)
			if(setaEsquerda = false){
				setaEsquerda = true;
			}
			if(setaEsquerda = true) {
				setaEsquerda = false;
			}
			if(setaEsquerda = false){
				setaEsquerda = true;
			}
			if(setaEsquerda = true) {
				setaEsquerda = false;
			}
	}

}
